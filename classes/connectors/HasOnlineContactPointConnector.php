<?php

use Opencontent\Ocopendata\Forms\Connectors\AbstractBaseConnector;

class HasOnlineContactPointConnector extends AbstractBaseConnector
{
    const REMOTE_ID_PREFIX = 'contact_';

    private $context;

    public static function generateRemoteId(int $objectId): string
    {
        return self::REMOTE_ID_PREFIX . $objectId;
    }

    public function runService($serviceIdentifier)
    {
        $this->context = self::findContext();
        return parent::runService($serviceIdentifier); // TODO: Change the autogenerated stub
    }

    private function findContext()
    {
        $organizationId = (int)$this->getHelper()->getParameter('context');
        if ($organizationId === 0){
            return null;
        }
        $organization = eZContentObject::fetch($organizationId);
        $canonicalRemoteID = self::generateRemoteId($organizationId);
        $canonicalContext = eZContentObject::fetchByRemoteID($canonicalRemoteID);
        if (!$canonicalContext instanceof eZContentObject && $organization instanceof eZContentObject) {
            $dataMap = $organization->dataMap();
            if (isset($dataMap['has_online_contact_point']) && $dataMap['has_online_contact_point']->hasContent()){
                $idList = explode('-', $dataMap['has_online_contact_point']->toString());
                foreach ($idList as $id) {
                    $contact = eZContentObject::fetch((int)$id);
                    if (!$contact instanceof eZContentObject){
                        continue;
                    }
                    if (strpos($contact->remoteID(), self::REMOTE_ID_PREFIX) !== false){
                        continue;
                    }
                    $relationsCount = (int)$contact->reverseRelatedObjectCount();
                    if ($relationsCount > 0){
                        continue;
                    }
                    $contact->setAttribute('remote_id', $canonicalRemoteID);
                    $contact->store();
                    $canonicalContext = $contact;
                    break;
                }
            }
        }

        return $canonicalContext;
    }

    protected function getData()
    {
        $contacts = [
            [
                'type' => '',
                'value' => '',
                'contact' => '',
            ],
        ];
        if ($this->context instanceof eZContentObject) {
            $locale = eZLocale::currentLocaleCode();
            $content = \Opencontent\Opendata\Api\Values\Content::createFromEzContentObject($this->context);
            $contacts = $content->data[$locale]['contact']['content'] ?? $content->data['ita-IT']['contact']['content'] ?? [];
        }
        return [
            'contacts' => $contacts,
        ];
    }

